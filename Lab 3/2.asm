Reset_Handler

	LDR R0, =SRC1
	LDR R0, [R0]
	LDR R1, =SRC2
	LDR R1, [R1]
LOOP CMP R0, R1
	SUBHI R0, R0, R1
	SUBLS R1, R1, R0
	BNE LOOP
	LDR R1, =ARR
	STR R0, [R1]

SRC1 DCD 0x18
SRC2 DCD 0x1E

	AREA MY_DATA, DATA, READWRITE
ARR DCD 0

	END