	AREA RESET, DATA, READONLY
	EXPORT __Vectors

__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	
	ALIGN
	
	AREA MY_CODE, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler
	
	LDR R0, =SRC
	LDR R1, [R0]
	MOV R2, #0x1
	BL FACT
	LDR R5, =ARR
	STR R2, [R5]
STOP B STOP
FACT CMP R1, #0x1
	BEQ EXIT
	PUSH {R1, LR}
	SUB R1, #0x1
	BL FACT
EXIT POP {R1, LR}
	MUL R2, R1
	BX LR

SRC DCD 0x5
	AREA MY_DATA, DATA, READWRITE
ARR DCD 0

	END