	AREA RESET, DATA, READONLY
	EXPORT __Vectors

__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	
	ALIGN
	
	AREA MY_CODE, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler

	MOV R0, #0x0
	LDR R1, =SRC
LOOP0 CMP R0, (N-1)
	BHS EXIT
	MOV R2, R1
	ADD R3, R0, #0x1
	ADD R4, R1, #0x4
LOOP1 CMP R3, N
	BHS SKIP
	LDR R5, [R2]
	LDR R6, [R4]
	CMP R6, R5
	BHS GOTO
	MOV R2, R4
GOTO ADD R3, #0x1
	ADD R4, #0x4
	B LOOP1
SKIP LDR R5, [R2]
	LDR R7, [R1]
	STR R5, [R1]
	STR R7, [R2]
	ADD R0, #0x1
	ADD R1, #0x4
	B LOOP0
EXIT STOP EXIT

N EQU 0xA
	AREA MY_DATA, DATA, READWRITE
SRC DCD 0, 0, 0, 0, 0

	END