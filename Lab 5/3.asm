	AREA RESET, DATA, READONLY
	EXPORT __Vectors

__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	
	ALIGN
	
	AREA MY_CODE, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
	
Reset_Handler

	MOV R0, #0xA
	LDR R1, =SRC
	MOV R2, 0x6
	LDR R4, =ARR
LOOP LDR R3, [R1], #0x4
	CMP R2, R3
	BEQ EXIT0
	SUBS R0, #0x1
	BNE LOOP
	BEQ EXIT1
EXIT0 MOV R5, #0x1
	STR R5, [R4]
	B STOP
EXIT1 MOV R5, #0x0
	STR R5, [R4]
STOP B STOP

SRC DCD 0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7, 0x8, 0x9, 0xA
	AREA MY_DATA, DATA, READWRITE
ARR DCD 0

	END